// Mutex handle
SemaphoreHandle_t xMutex;

void send_uart(char *msg)
{
    HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
}

// Task 1: Toggle LED1
void Task1(void *pvParameters)
{
    for(;;)
    {
        if(xSemaphoreTake(xMutex, portMAX_DELAY) == pdTRUE)
        {
            HAL_GPIO_TogglePin(GPIOI, GPIO_PIN_1);  // Toggle LED1
            send_uart("Task1: LED1 toggled\r\n");
            vTaskDelay(pdMS_TO_TICKS(500));         // Simulate work
            xSemaphoreGive(xMutex);
        }
        vTaskDelay(pdMS_TO_TICKS(100));             // Small delay
    }
}

// Task 2: Toggle LED2
void Task2(void *pvParameters)
{
    for(;;)
    {
        if(xSemaphoreTake(xMutex, portMAX_DELAY) == pdTRUE)
        {
            HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_4);  // Toggle LED2
            send_uart("Task2: LED2 toggled\r\n");
            vTaskDelay(pdMS_TO_TICKS(500));
            xSemaphoreGive(xMutex);
        }
        vTaskDelay(pdMS_TO_TICKS(100));
    }
}

// Task 3: Print message only if mutex available
void Task3(void *pvParameters)
{
    for(;;)
    {
        if(xSemaphoreTake(xMutex, portMAX_DELAY) == pdTRUE)   // Non-blocking
        {
            send_uart("Task3: Mutex acquired, printing message\r\n");
            xSemaphoreGive(xMutex);
        }
        else
        {
            send_uart("Task3: Mutex busy, skipping\r\n");
        }
        vTaskDelay(pdMS_TO_TICKS(300));
    }
}

