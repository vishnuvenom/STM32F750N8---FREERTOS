int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_USART1_UART_Init();

  /* Create Queue */
  buttonQueue = xQueueCreate(10, sizeof(uint32_t));  // Queue for button events
  if(buttonQueue == NULL) {
      Error_Handler(); // Queue not created
  }

  /* Create task */
  xTaskCreate(Task_ButtonHandler, "BtnTask", 256, NULL, 2, NULL);

  /* Start scheduler */
  vTaskStartScheduler();

  while (1) {}
}

/* === TASK IMPLEMENTATION === */
void Task_ButtonHandler(void *argument)
{
    uint32_t button_event;
    char msg[50];

    for(;;)
    {
        if(xQueueReceive(buttonQueue, &button_event, portMAX_DELAY) == pdPASS)
        {
            snprintf(msg, sizeof(msg), "Button pressed! Value=%lu\r\n", button_event);
            HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
        }
    }
}

/* === INTERRUPT CALLBACK === */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;
    uint32_t eventValue = HAL_GetTick(); // just send tick count as event

    if(GPIO_Pin == GPIO_PIN_11) // Assume button on PA0
    {
        xQueueSendFromISR(buttonQueue, &eventValue, &xHigherPriorityTaskWoken);

        // if a higher priority task was woken, request context switch
        portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
    }
}



